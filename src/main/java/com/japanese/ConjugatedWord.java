package com.japanese;

public class ConjugatedWord {
	
	String conjugation;
	String word;
	

	public ConjugatedWord(String conjugation, String word) {
		super();
		this.conjugation = conjugation;
		this.word = word;
	}

	
	public String getConjugation() {
		return conjugation;
	}

	public String getWord() {
		return word;
	}
	
}
